package B_Операции.TernaryOperation_ТернарнаяОперация;

public class TernaryOperation_ТернарнаяОперация {
    public static void main(String[] args) {
        //Тернарная операция
        System.out.println("Тернарная операция");
        int число = 10;
        String результат = (число > 5) ? "Число больше 5" : "Число не больше 5";

        System.out.println(результат); // Выводит "Число больше 5", так как число больше 5
    }
}
